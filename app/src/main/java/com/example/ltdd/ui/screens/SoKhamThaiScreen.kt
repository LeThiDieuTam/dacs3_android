@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun SoKhamThaiScreen(navController: NavHostController, userId: String) {
    val context = LocalContext.current
    val coroutineScope = rememberCoroutineScope()

    var date by rememberSaveable { mutableStateOf("") }
    var doctorName by rememberSaveable { mutableStateOf("") }
    var gestationalAge by rememberSaveable { mutableStateOf("") }
    var weight by rememberSaveable { mutableStateOf("") }
    var bloodPressure by rememberSaveable { mutableStateOf("") }
    var fetalHeartRate by rememberSaveable { mutableStateOf("") }
    var note by rememberSaveable { mutableStateOf("") }

    var isEditing by rememberSaveable { mutableStateOf(false) }
    var editId by rememberSaveable { mutableStateOf("") }
    var showForm by rememberSaveable { mutableStateOf(false) }
    var list by remember { mutableStateOf(listOf<Visit>()) } // Thay ƒë·ªïi ki·ªÉu d·ªØ li·ªáu t·ª´ JSONObject sang Visit

    val pastelPink = Color(0xFFFFC1CC)
    val pastelGreen = Color(0xFFB2F7EF)
    val pastelBackground = Color(0xFFFDF6F6)

    fun fetchList() {
        coroutineScope.launch(Dispatchers.IO) {
            try {

                val fetchedList = RetrofitClient.instance.getVisits(userId)
                withContext(Dispatchers.Main) {
                    list = fetchedList
                }
            } catch (e: Exception) {
                withContext(Dispatchers.Main) {
                    val message = when (e) {
                        is IOException -> "L·ªói m·∫°ng: Vui l√≤ng ki·ªÉm tra k·∫øt n·ªëi internet."
                        is HttpException -> "L·ªói server: ${e.code()}. Vui l√≤ng th·ª≠ l·∫°i sau."
                        else -> "ƒê√£ x·∫£y ra l·ªói kh√¥ng x√°c ƒë·ªãnh: ${e.localizedMessage}"
                    }
                    Toast.makeText(context, message, Toast.LENGTH_SHORT).show()
                    e.printStackTrace()
                }
            }
        }
    }

    fun clearForm() {
        date = ""; doctorName = ""; gestationalAge = ""; weight = ""
        bloodPressure = ""; fetalHeartRate = ""; note = ""
        isEditing = false; editId = ""; showForm = false
    }

    fun sendForm(id: String?) {
        coroutineScope.launch(Dispatchers.IO) {
            try {
                val response = if (id == null) {
                    // G·ªçi API th√™m m·ªõi
                    RetrofitClient.instance.addVisit(
                        userId, date, doctorName, gestationalAge, weight, bloodPressure, fetalHeartRate, note
                    )
                } else {
                    RetrofitClient.instance.updateVisit(
                        id, userId, date, doctorName, gestationalAge, weight, bloodPressure, fetalHeartRate, note
                    )
                }

                withContext(Dispatchers.Main) {
                    Toast.makeText(context, response.message, Toast.LENGTH_SHORT).show()
                    if (response.success) {
                        clearForm()
                        fetchList()
                    }
                }
            } catch (e: Exception) {
                withContext(Dispatchers.Main) {
                    val message = when (e) {
                        is IOException -> "L·ªói k·∫øt n·ªëi: Kh√¥ng th·ªÉ g·ª≠i y√™u c·∫ßu ƒë·∫øn server."
                        is HttpException -> "L·ªói ph·∫£n h·ªìi t·ª´ server: ${e.code()}. ${e.response()?.errorBody()?.string()}" // L·∫•y th√™m th√¥ng tin l·ªói t·ª´ body n·∫øu c√≥
                        else -> "ƒê√£ x·∫£y ra l·ªói: ${e.localizedMessage}"
                    }
                    Toast.makeText(context, message, Toast.LENGTH_LONG).show()
                    e.printStackTrace()
                }
            }
        }
    }


    fun sendDelete(id: String) {
        coroutineScope.launch(Dispatchers.IO) {
            try {
                // G·ªçi API x√≥a
                val response = RetrofitClient.instance.deleteVisit(userId, id)
                withContext(Dispatchers.Main) {
                    Toast.makeText(context, response.message, Toast.LENGTH_SHORT).show()
                    if (response.success) {
                        fetchList()
                    }
                }
            } catch (e: Exception) {
                withContext(Dispatchers.Main) {
                    val message = when (e) {
                        is IOException -> "L·ªói k·∫øt n·ªëi: Kh√¥ng th·ªÉ x√≥a s·ªï kh√°m thai."
                        is HttpException -> "L·ªói server khi x√≥a: ${e.code()}"
                        else -> "ƒê√£ x·∫£y ra l·ªói: ${e.localizedMessage}"
                    }
                    Toast.makeText(context, message, Toast.LENGTH_SHORT).show()
                    e.printStackTrace()
                }
            }
        }
    }

    LaunchedEffect(Unit) { fetchList() }

    Surface(modifier = Modifier.fillMaxSize(), color = pastelBackground) {
        LazyColumn(modifier = Modifier.fillMaxSize().padding(16.dp), verticalArrangement = Arrangement.spacedBy(12.dp)) {
            item {
                Row(
                    modifier = Modifier.fillMaxWidth(),
                    verticalAlignment = Alignment.CenterVertically,
                    horizontalArrangement = Arrangement.SpaceBetween
                ) {
                    IconButton(onClick = { navController.popBackStack() }) {
                        Icon(
                            imageVector = Icons.AutoMirrored.Filled.ArrowBack,
                            contentDescription = "Quay l·∫°i",
                            tint = Color(0xFF4C4C6D)
                        )
                    }

                    Text(
                        "S·ªï kh√°m thai",
                        style = MaterialTheme.typography.headlineSmall.copy(color = Color(0xFF4C4C6D)),
                        modifier = Modifier.weight(1f),
                    )

                    Button(
                        onClick = { clearForm(); showForm = true },
                        colors = ButtonDefaults.buttonColors(containerColor = pastelPink),
                        shape = RoundedCornerShape(16.dp)
                    ) {
                        Text("‚ûï Th√™m s·ªï kh√°m thai", color = Color.White)
                    }
                }
            }


            items(list) { item ->
                Card(
                    modifier = Modifier.fillMaxWidth(),
                    colors = CardDefaults.cardColors(containerColor = pastelGreen),
                    shape = RoundedCornerShape(20.dp),
                    elevation = CardDefaults.cardElevation(6.dp)
                ) {
                    Column(modifier = Modifier.padding(12.dp)) {
                        Text("üìÖ Ng√†y kh√°m: ${item.visitDate}")
                        Text("üë©‚Äç‚öïÔ∏è B√°c sƒ©: ${item.doctorName}")
                        Text("üçº Tu·ªïi thai: ${item.gestationalAge} tu·∫ßn")
                        Text("‚öñÔ∏è C√¢n n·∫∑ng: ${item.weight} kg")
                        Text("üíì Huy·∫øt √°p: ${item.bloodPressure}")
                        Text("‚ù§Ô∏è Tim thai: ${item.fetalHeartRate}")
                        Text("üìù Ghi ch√∫: ${item.notes ?: "Kh√¥ng c√≥"}")
                        Row(modifier = Modifier.padding(top = 8.dp)) {
                            IconButton(onClick = {
                                date = item.visitDate
                                doctorName = item.doctorName
                                gestationalAge = item.gestationalAge
                                weight = item.weight
                                bloodPressure = item.bloodPressure
                                fetalHeartRate = item.fetalHeartRate
                                note = item.notes ?: ""
                                editId = item.visitId
                                showForm = true
                            }) {
                                Icon(Icons.Default.Edit, contentDescription = "S·ª≠a", tint = Color(0xFF4C4C6D))
                            }
                            IconButton(onClick = {
                                sendDelete(item.visitId)
                            }) {
                                Icon(Icons.Default.Delete, contentDescription = "Xo√°", tint = Color.Red)
                            }
                        }
                    }
                }
            }

            if (showForm) {
                item {
                    Divider(Modifier.padding(vertical = 12.dp))
                    Text(
                        if (isEditing) "üõ†Ô∏è C·∫≠p nh·∫≠t th√¥ng tin" else "üìù Th√™m s·ªï kh√°m thai",
                        style = MaterialTheme.typography.titleMedium,
                        color = Color(0xFF4C4C6D)
                    )
                    Spacer(Modifier.height(8.dp))
                    FormField("üìÖ Ng√†y kh√°m (yyyy-mm-dd)", date) { date = it }
                    FormField("üë©‚Äç‚öïÔ∏è B√°c sƒ© kh√°m", doctorName) { doctorName = it }
                    FormField("üçº Tu·ªïi thai (tu·∫ßn)", gestationalAge) { gestationalAge = it }
                    FormField("‚öñÔ∏è C√¢n n·∫∑ng (kg)", weight) { weight = it }
                    FormField("üíì Huy·∫øt √°p", bloodPressure) { bloodPressure = it }
                    FormField("‚ù§Ô∏è Tim thai", fetalHeartRate) { fetalHeartRate = it }
                    FormField("üìù Ghi ch√∫", note) { note = it }
                    Spacer(Modifier.height(8.dp))
                    Row {
                        Button(
                            onClick = {
                                if (date.isBlank() || doctorName.isBlank() || gestationalAge.isBlank() || weight.isBlank() || bloodPressure.isBlank() || fetalHeartRate.isBlank()) {
                                    Toast.makeText(context, "Vui l√≤ng ƒëi·ªÅn ƒë·∫ßy ƒë·ªß th√¥ng tin b·∫Øt bu·ªôc", Toast.LENGTH_SHORT).show()
                                    return@Button
                                }
                                sendForm(if (isEditing) editId else null)
                            },
                            colors = ButtonDefaults.buttonColors(containerColor = pastelPink),
                            shape = RoundedCornerShape(12.dp)
                        ) {
                            Text(if (isEditing) "‚úÖ C·∫≠p nh·∫≠t" else "‚ûï Th√™m m·ªõi", color = Color.White)
                        }
                        Spacer(modifier = Modifier.width(8.dp))
                        OutlinedButton(
                            onClick = { clearForm() },
                            shape = RoundedCornerShape(12.dp),
                            colors = ButtonDefaults.outlinedButtonColors(contentColor = Color(0xFF4C4C6D))
                        ) {
                            Text("‚ùå Hu·ª∑")
                        }
                    }
                }
            }
        }
    }
}

@Composable
fun FormField(label: String, value: String, onValueChange: (String) -> Unit) {
    OutlinedTextField(
        value = value,
        onValueChange = onValueChange,
        label = { Text(label) },
        modifier = Modifier
            .fillMaxWidth()
            .padding(vertical = 4.dp),
        colors = OutlinedTextFieldDefaults.colors(
            focusedBorderColor = Color(0xFFFFC1CC),
            unfocusedBorderColor = Color(0xFFB2F7EF),
            focusedLabelColor = Color(0xFF4C4C6D),
            unfocusedLabelColor = Color(0xFF4C4C6D)
        ),
        shape = RoundedCornerShape(8.dp)
    )
}

